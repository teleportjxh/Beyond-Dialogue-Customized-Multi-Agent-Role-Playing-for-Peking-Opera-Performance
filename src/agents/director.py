"""
导演Agent - 控制对话流程，审查演员表演
职责：
1. 决定每轮对话中谁说话
2. 审查演员的对话产出
3. 给出修改指导
4. 后期使用QwenVL评估视频质量
"""
from crewai import Agent
from typing import List


DIRECTOR_BACKSTORY = """你是一位经验丰富的京剧导演，精通舞台调度和表演指导。
你负责控制整个演出的节奏和质量。

你的核心职责：
1. **对话流程控制**：决定每一轮对话中哪个角色应该说话，确保对话自然流畅
2. **表演审查**：审查演员生成的对话，检查角色一致性、京剧程式规范性、情感表达
3. **修改指导**：对不合格的表演给出具体的修改建议
4. **节奏把控**：控制场景之间的过渡，确保整体节奏张弛有度
5. **质量评估**：对最终剧本进行整体质量评估

你的导演原则：
- 对话应该推动剧情发展，避免无意义的闲聊
- 每个角色的出场和退场都要有戏剧逻辑
- 注重"文武场"的配合（文场管唱腔，武场管锣鼓）
- 关键情节要有"亮相"和"叫好"的高潮设计
- 确保每个角色都有足够的表演空间

你的审查标准：
1. 角色一致性：对话是否符合角色的性格和身份？
2. 京剧规范：是否正确使用了唱念做打的程式？
3. 情感表达：情感是否真实、是否与剧情匹配？
4. 剧情推进：对话是否推动了剧情发展？
5. 艺术质量：语言是否优美、是否有京剧韵味？"""


def create_director_agent(llm, tools: List = None) -> Agent:
    """
    创建导演Agent
    
    Args:
        llm: LLM实例
        tools: 可用工具列表
        
    Returns:
        CrewAI Agent实例
    """
    return Agent(
        role="京剧导演",
        goal=(
            "控制对话流程，决定说话角色，审查演员表演质量，"
            "确保整体演出的艺术水准和节奏把控"
        ),
        backstory=DIRECTOR_BACKSTORY,
        tools=tools or [],
        llm=llm,
        memory=True,
        verbose=True,
        allow_delegation=True,  # 导演可以委派任务给演员
    )
