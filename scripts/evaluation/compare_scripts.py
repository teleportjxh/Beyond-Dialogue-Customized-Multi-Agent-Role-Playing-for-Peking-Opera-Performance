def print_section(title, content, max_lines=30):
    """打印一个部分"""
    print(f"\n{'='*70}")
    print(f"{title:^70}")
    print('='*70)
    lines = content.split('\n')
    for i, line in enumerate(lines[:max_lines], 1):
        print(f"{i:3d} | {line}")
    if len(lines) > max_lines:
        print(f"... (还有 {len(lines) - max_lines} 行)")


